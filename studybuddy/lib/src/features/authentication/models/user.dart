class User {
  final String imagePath;
  final String name;
  final String email;
  final String about;
  final bool isDarkMode;
  final String classYear;
  final String Major;

  const User({
    required this.imagePath,
    required this.name,
    required this.email,
    required this.about,
    required this.classYear,
    required this.Major,
    required this.isDarkMode,
  });
}
